# The “in” Operator:

# Imagine you have a treasure chest filled with colorful marbles. 🌈
# The “in” operator helps you check if a particular marble is inside the chest.
chest = ["red", "blue", "green", "yellow"]
if "green" in chest:
    print("Found the green marble!")

# The “not in” Operator:

# Now, let’s talk about the opposite. Imagine you’re playing hide-and-seek with your toys. 🙈
# The “not in” operator helps you check if a toy is not hiding somewhere.
toys = ["teddy bear", "ball", "car"]
if "dinosaur" not in toys:
    print("Nope, not under the bed!")