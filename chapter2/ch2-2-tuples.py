# How Do Tuples Work?

# Ordered Collection
# No Changes Allowed

# Creating a Tuple:
# Use round brackets () to create a tuple. Separate items with commas.
favorite_toys = ("teddy bear", "lego", "dinosaur")

# Getting Your Toys:
# Access specific items by their index (starting from 0).
print(favorite_toys[1])  # Output: "lego"

# Counting Toys:
# Use len() to find out how many items are in the tuple.
print(len(favorite_toys))  # Output: 3
