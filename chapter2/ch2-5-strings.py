# Strings are made up of individual characters (letters, numbers, or symbols).
# You can create a string using either single quotes ('') or double quotes ("").

# Fun String Tricks

# Combining Strings (Concatenation):
# Join strings together to create new ones.

first_name = "Alice"
last_name = "Smith"
full_name = first_name + " " + last_name
print(full_name)  # Output: "Alice Smith"

# Changing Letters (Indexing):
# Access specific characters by their position.
word = "Python"
print(word[0])  # Output: "P"
print(word[2])  # Output: "t"

# Finding the Length (Len):
# Count the number of characters in a string.
message = "I love coding"
print(len(message))  # Output: 12