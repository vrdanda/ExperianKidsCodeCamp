# Algorithm for Making a Peanut Butter and Jelly Sandwich:​
#   Gather your ingredients: You’ll need bread, peanut butter, and jelly.
#   Spread peanut butter: Take one slice of bread and spread peanut butter on it.
#   Spread jelly: Now take the other slice of bread and spread jelly on it.
#   Combine the slices: Put the two slices of bread together, 
#      with the peanut butter and jelly sides facing each other.
#   Cut the sandwich: Finally, cut the sandwich in half (if you like!).
#
# And here’s the same algorithm coded in Python:

def make_pbj_sandwich():
    # Step 1: Gather ingredients​
    bread = "whole wheat bread"
    peanut_butter = "smooth peanut butter"
    jelly = "grape jelly"

    # Step 2: Spread peanut butter​
    print(f"Spread {peanut_butter} on one slice of {bread}.")

    # Step 3: Spread jelly​
    print(f"Spread {jelly} on the other slice of {bread}.")

    # Step 4: Combine the slices​
    print(f"Put the two slices of {bread} together.")

    # Step 5: Cut the sandwich​
    print("Cut the sandwich in half.")

# Let's make a sandwich!​
make_pbj_sandwich()